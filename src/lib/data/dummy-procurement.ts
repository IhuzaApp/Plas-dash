export interface Supplier {
  id: string;
  name: string;
  companyName?: string; // Adding companyName as requested in form
  contactPerson: string;
  email: string;
  phone: string;
  address?: string;
  taxId?: string;
  category: string;
  status: 'Active' | 'Inactive' | 'Pending';
  rating: number; // 1-5
  leadTimeDays: number;
  paymentTerms: 'Cash' | '30 Days' | '60 Days';
  outstandingBalance: number;
  creditLimit?: number;
  notes?: string;
}

export interface Product {
  id: string;
  sku: string;
  name: string;
  category: string;
  supplierId: string;
  unitPrice: number;
  stockUnit: string;
  minOrderQuantity: number;
}

export interface Quotation {
  id: string;
  rfqNumber: string;
  supplierId: string;
  dateRequested: string;
  dateValidUntil: string;
  status: 'Draft' | 'Sent' | 'Received' | 'Rejected' | 'Accepted';
  totalAmount: number | null;
  items: { productId: string; quantity: number; unitPrice?: number }[];
  notes?: string;
}

export interface PurchaseOrder {
  id: string;
  poNumber: string;
  supplierId: string;
  dateCreated: string;
  expectedDeliveryDate: string;
  status: 'Pending' | 'Approved' | 'Shipped' | 'Delivered' | 'Cancelled';
  deliveryStatus: 'Pending' | 'Partial' | 'Received';
  paymentStatus: 'Unpaid' | 'Partial' | 'Paid';
  taxPercentage?: number;
  discount?: number;
  paymentTerms?: string;
  totalAmount: number;
  items: { productId: string; quantity: number; unitPrice: number }[];
}

